#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			return ((char *)s + 1);
		i++;
	}
	if (s[i] == '\0' && c == '\0')
		return ((char *)s + 1);
	return (0);
}
